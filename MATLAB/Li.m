function [a] = Li(x)
    a = x;
    % Face Rotation
    a(6,4) = x(4,4);
    a(5,4) = x(4,5);
    a(4,4) = x(4,6);
    a(6,5) = x(5,4);
    a(4,5) = x(5,6);
    a(6,6) = x(6,4);
    a(5,6) = x(6,5);
    a(4,6) = x(6,6);
    % Side Rotation
    a(6,3) = x(1,7);
    a(5,3) = x(2,7);
    a(4,3) = x(3,7);
    a(1,7) = x(4,7);
    a(2,7) = x(5,7);
    a(3,7) = x(6,7);
    a(6,7) = x(9,7);
    a(5,7) = x(8,7);
    a(4,7) = x(7,7);
    a(9,7) = x(4,3);
    a(8,7) = x(5,3);
    a(7,7) = x(6,3);



end