function [a] = Fi(x)
    a = x;
    % Face Rotation
    a(4,7) = x(4,9);
    a(4,8) = x(5,9);
    a(4,9) = x(6,9);
    a(5,7) = x(4,8);
    a(5,9) = x(6,8);
    a(6,7) = x(4,7);
    a(6,8) = x(5,7);
    a(6,9) = x(6,7);
    % Side Rotation
    a(6,6) = x(3,7);
    a(5,6) = x(3,8);
    a(4,6) = x(3,9);
    a(3,7) = x(4,10);
    a(3,8) = x(5,10);
    a(3,9) = x(6,10);
    a(6,10) = x(7,7);
    a(5,10) = x(7,8);
    a(4,10) = x(7,9);
    a(7,9) = x(6,6);
    a(7,8) = x(5,6);
    a(7,7) = x(4,6);



end

